import os
import json
from openai import OpenAI
from dotenv import load_dotenv

load_dotenv()

client = OpenAI(
    api_key=os.getenv("BAILIAN_API_KEY"),
    base_url="https://dashscope.aliyuncs.com/compatible-mode/v1"
)

# ==================== v0.8 加强版 System Prompt ====================
SYSTEM_PROMPT = """你是专业的初中语文老师，批改作文时必须严格、专业、客观。

【最高铁律】
- 你**只能且必须**返回一个**纯净、有效的JSON对象**。
- 绝对不要输出任何其他文字、解释、换行符、markdown、代码块或多余内容。
- JSON必须可以被直接解析，不能有格式错误。

输出格式必须严格为以下JSON（所有字段都必须存在）：
{
  "总分": 整数,
  "维度评分": {
    "内容与主题": "分数/25",
    "结构与逻辑": "分数/25",
    "语言表达": "分数/25",
    "创新与情感": "分数/25"
  },
  "优点": ["优点1", "优点2"],
  "不足": ["不足1"],
  "改进建议": "3-5句具体可操作的专业建议",
  "启发问题": ["问题1", "问题2", "问题3"],
  "鼓励语": "温暖且有力量的鼓励话语"
}

现在开始批改作文，请严格只返回上面的JSON格式。
"""

def test_qwen_api(作文内容: str, model: str = "qwen-turbo"):
    user_prompt = f"请严格按照JSON格式批改以下作文：\n{作文内容}"

    response = client.chat.completions.create(
        model=model,
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": user_prompt}
        ],
        temperature=0.55,   # 进一步降低随机性
        max_tokens=1200,
    )
    
    raw_text = response.choices[0].message.content.strip()
    
    # ==================== 增强解析逻辑 ====================
    try:
        # 清理可能的多余内容
        if "```json" in raw_text:
            raw_text = raw_text.split("```json")[1].split("```")[0]
        elif "```" in raw_text:
            raw_text = raw_text.split("```")[1]
        
        # 尝试直接解析
        result = json.loads(raw_text.strip())
        return result
        
    except json.JSONDecodeError:
        # 如果解析失败，尝试更激进的清理
        try:
            # 移除所有换行和多余空格
            cleaned = raw_text.replace('\n', '').replace('\r', '').strip()
            result = json.loads(cleaned)
            return result
        except:
            return {
                "error": "JSON解析失败",
                "raw_output": raw_text[:800]  # 只显示前800字符
            }


